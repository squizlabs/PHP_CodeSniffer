/**
 * Checks that only one thing is declared per file.
 *
 * @category  PHP
 * @package   PHP_CodeSniffer
 * @author    Andy Grunwald <andygrunwald@gmail.com>
 * @copyright 2010 Andy Grunwald
 * @license   https://github.com/squizlabs/PHP_CodeSniffer/blob/master/licence.txt BSD Licence
 * @version   Release: 1.5.0RC1
 * @link      http://pear.php.net/package/PHP_CodeSniffer
 */
abstract class Generic_Sniffs_Files_OnePerFileSniff implements PHP_CodeSniffer_Sniff
{


    /**
     * Processes this sniff, when one of its tokens is encountered.
     *
     * @param PHP_CodeSniffer_File $phpcsFile The file being scanned.
     * @param int                  $stackPtr  The position of the current token in
     *                                        the stack passed in $tokens.
     *
     * @return void
     */
    public function process(PHP_CodeSniffer_File $phpcsFile, $stackPtr)
    {
        $next = $phpcsFile->findNext($this->register(), ($stackPtr + 1));
        if ($next !== false) {
            $tokens = $phpcsFile->getTokens();
            $error = 'Only one ' . $tokens[$next]['content'] . ' is allowed in a file';
            $phpcsFile->addError($error, $next, 'MultipleFound');
        }

    }//end process()


}//end class

